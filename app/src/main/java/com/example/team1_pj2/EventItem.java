package com.example.team1_pj2;

public class EventItem {
    int id;
    String title;
    String startDay;
    String endDay;

    public EventItem(int id, String title, String startDay, String endDay) {
        this.id = id;
        this.title = title;
        this.startDay = startDay;
        this.endDay = endDay;
    }

    public EventItem() {}

}
